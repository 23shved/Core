package ua.lviv.shved;

public class ApplicationSwitch {
public static void main(String[] args) {
	
	
	int  a = 22;
	
	switch (a) {
	case 12: {
		System.out.println("12");
	break;
	}
	case 22: {
		System.out.println("13");
		break;
		}
	case 16: {
		System.out.println("16");
		break;
		}
	default:
		System.out.println("default");
		break;
	}
}
}
